class SwitchCase{
    public static void main(String[] args) {
        int x = 2;
        switch (x){
            case 1 :
                System.out.println("this is 1");
                break;
            case 2 :
                System.out.println("this is 2");
                break;
            default:
                System.out.println("not is 1,2");
        }
    }
}